export interface Adresse {
  /**
   * Represents an identification number for an address.
   *
   * @typedef {number} idadresse
   */
  idadresse: number;
  /**
   * Represents the variable 'cp' which is a number.
   *
   * @typedef {number} cp
   */
  cp: number;
  /**
   * Represents a locality.
   * @typedef {string} Localite
   */
  localite:string;
  /**
   * Represents a variable named rue.
   *
   * @type {string}
   */
  rue:string;
  /**
   * Represents a string value called 'num'.
   *
   * @typedef {string} num
   */
  num:string;
}
